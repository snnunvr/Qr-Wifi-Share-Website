function changeLanguage(lang) {
    const texts = translations[lang];
    if (!texts) return;

    // Update Language Selection Visuals if needed
    document.documentElement.lang = lang;
    if (lang === 'ar') {
        document.body.style.direction = 'rtl';
    } else {
        document.body.style.direction = 'ltr';
    }

    // Map IDs to Translation Keys
    document.title = texts.title;
    safeSetText('nav-home', texts.home);
    safeSetText('nav-privacy', texts.privacy);
    safeSetText('hero-title', texts.heroTitle);
    safeSetText('hero-desc', texts.heroDesc);
    safeSetText('download-btn', texts.downloadBtn);
    safeSetText('features-title', texts.featuresTitle);

    // Features loop
    for (let i = 1; i <= 6; i++) {
        safeSetText(`f${i}-title`, texts[`feature${i}Title`]);
        safeSetText(`f${i}-desc`, texts[`feature${i}Desc`]);
    }

    safeSetText('footer-rights', texts.footerRights);
    safeSetText('nav-contact', texts.contact);
    safeSetText('footer-privacy', texts.privacy);
    safeSetText('footer-contact', texts.contact);
}

function safeSetText(id, text) {
    const el = document.getElementById(id);
    if (el) el.innerText = text;
}

