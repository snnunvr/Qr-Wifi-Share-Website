    if (!texts) return;

    document.documentElement.lang = lang;
    if (lang === 'ar') document.body.style.direction = 'rtl';
    else document.body.style.direction = 'ltr';

    document.title = texts.title;

    // Helper functions
    const setText = (id, txt) => { const el = document.getElementById(id); if (el) el.innerText = txt; };
    const setHtml = (id, txt) => { const el = document.getElementById(id); if (el) el.innerHTML = txt; };

    // Navigation
    setText('nav-home', texts.home);
    setText('nav-privacy', texts.privacy);
    setText('nav-contact', texts.contact);
    setText('footer-rights', texts.footerRights);
    setText('footer-privacy', texts.privacy);
